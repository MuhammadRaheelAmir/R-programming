# R Operators

a <- 15
b <- 4

# Arithmetic operators
print(a + b)   # add
print(a - b)   # subtract
print(a * b)   # multiply
print(a / b)   # divide
print(a %% b)  # remainder
print(a ^ b)   # power

# Comparison operators
print(a > b)   # greater
print(a < b)   # less
print(a == b)  # equal
print(a != b)  # not equal

# Logical operators
x <- TRUE
y <- FALSE
print(x & y)   # AND
print(x | y)   # OR
print(!x)      # NOT
